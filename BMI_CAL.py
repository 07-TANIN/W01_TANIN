weight = float(input("กรุณากรอกน้ำหนัก (กิโลกรัม): "))
height_cm = float(input("กรุณากรอกส่วนสูง (เซนติเมตร): "))

# แปลงส่วนสูงจาก เซนติเมตร เป็น เมตร
height_m = height_cm / 100

bmi = weight / (height_m ** 2)

# ทศนิยม 2 ตำแหน่ง
print(f"\nค่า BMI ของคุณคือ: {bmi:.2f}")

if bmi < 18.5:
    print("ผลลัพธ์: น้ำหนักน้อยกว่ามาตรฐาน (ผอม)")
elif 18.5 <= bmi < 23.0:
    print("ผลลัพธ์: น้ำหนักปกติ (สุขภาพดี)")
elif 23.0 <= bmi < 25.0:
    print("ผลลัพธ์: น้ำหนักเกิน (ท้วม)")
elif 25.0 <= bmi < 30.0:
    print("ผลลัพธ์: อ้วน (ระดับ 1)")
else:
    print("ผลลัพธ์: อ้วนมาก (ระดับ 2)")